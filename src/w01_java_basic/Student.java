
package w01_java_basic;

/**
 *
 * @author hp
 */
public class Student
{
    private String name;
    private int Id;

    /**
     * @return the name
     */
    public String getName() 
    {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String n) 
    {
        name = n;
    }

    /**
     * @return the Id
     */
    public int getId() 
    {
        return Id;
    }

    /**
     * @param Id the Id to set
     */
    public void setId(int Id) {
        this.Id = Id;
    }
}
